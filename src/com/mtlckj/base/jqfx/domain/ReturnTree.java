package com.mtlckj.base.jqfx.domain;

public class ReturnTree {

	private String dm;
	private String ct;
	public String getDm() {
		return dm;
	}
	public void setDm(String dm) {
		this.dm = dm;
	}
	public String getCt() {
		return ct;
	}
	public void setCt(String ct) {
		this.ct = ct;
	}
	@Override
	public String toString() {
		return "ReturnTree [dm=" + dm + ", ct=" + ct + "]";
	}
	
}
